package com.aameen.logicbuilding;

public class StarPattern2 {

	public static void main(String[] args) {
						/*Star Pattern 2
				
						* * * * * 
				
						* * * * 
				
						* * * 
				
						* * 
				
						*   */
						
		
		for(int i=5;i>=1;i--)
		{
			for(int j=i;j>=1;j--)
			{
				System.out.print("*");
			}
			System.out.println("");
			
		}
		

	}

}
